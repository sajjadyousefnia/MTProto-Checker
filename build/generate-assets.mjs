import { readFileSync, writeFileSync, readdirSync, statSync } from 'fs';
import { join, relative, sep } from 'path';
import { fileURLToPath } from 'url';

const __dirname = join(fileURLToPath(import.meta.url), '..');
const publicDir = join(__dirname, '..', 'public');
const outFile = join(__dirname, 'assets.mjs');

function walk(dir) {
    const files = [];
    for (const entry of readdirSync(dir)) {
        const full = join(dir, entry);
        if (statSync(full).isDirectory()) {
            files.push(...walk(full));
        } else {
            const rel = relative(publicDir, full).replace(sep, '/');
            const data = readFileSync(full);
            files.push({ path: '/' + rel, data: data.toString('base64'), mime: getMime(rel) });
        }
    }
    return files;
}

function getMime(p) {
    if (p.endsWith('.html')) return 'text/html;charset=utf-8';
    if (p.endsWith('.css')) return 'text/css;charset=utf-8';
    if (p.endsWith('.js')) return 'application/javascript;charset=utf-8';
    if (p.endsWith('.svg')) return 'image/svg+xml';
    if (p.endsWith('.png')) return 'image/png';
    if (p.endsWith('.ico')) return 'image/x-icon';
    return 'application/octet-stream';
}

const files = walk(publicDir);
const code = `// auto-generated by build/generate-assets.mjs
const assets = ${JSON.stringify(files, null, 2)};
export default assets;
`;

writeFileSync(outFile, code, 'utf-8');
console.log(`Generated ${outFile} (${files.length} files)`);
